const mongoose = require('mongoose');

const Person = mongoose.model('Person', {
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
    },
    password: {
        type: String,
        required: true,
        select: false,
    },
});

module.exports = Person;